    private void jButt_AGREGARPRODUCTOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt_AGREGARPRODUCTOActionPerformed
        try{
            
            if(this.jText_Nombre.getText().isEmpty() || this.jText_DES.getText().isEmpty() || this.jText_PRECIO.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Falta información en algunos campos!");
            }else{
                this.jButt_BORRAR.setEnabled(true);
                if(AgregarMásProductos == true){
                    
                    int dato = 0;
                    boolean agregar = true;
                    for(int i = 0; i < modeloTabla.getRowCount(); i++){
                        dato = Integer.parseInt(this.jTab_PRODUCTOS.getValueAt(i, 0).toString());
                        if(Integer.parseInt(this.jSpinn_ID.getValue().toString()) == dato){
                            agregar = false;
                            JOptionPane.showMessageDialog(null, "Ya existe un producto con este ID: "+this.jSpinn_ID.getValue()+". Por favor seleccione otro.");
                        }
                    }
                    
//                    if(Integer.parseInt(this.jSpinn_ID.getValue().toString()) == dato){
//                            JOptionPane.showMessageDialog(null, "Ya existe un producto con este ID: "+this.jSpinn_ID.getValue()+". Por favor seleccione otro.");
//                        }else{
                    if(agregar == true && AgregarMásProductos == true){  
                     modeloTabla.insertRow(modeloTabla.getRowCount(), new Object[]{
                     this.jSpinn_ID.getValue(), this.jText_Nombre.getText(),
                     this.jText_DES.getText(), this.jText_PRECIO.getText()});
                    }   
                }else if(AgregarMásProductos == false){
                 modeloTabla.insertRow(modeloTabla.getRowCount(), new Object[]{
                 this.jSpinn_ID.getValue(), this.jText_Nombre.getText(),
                 this.jText_DES.getText(), this.jText_PRECIO.getText()});
                 AgregarMásProductos = true;
                }
            } 
        }catch(RuntimeException ex){
            
        }
    }//GEN-LAST:event_jButt_AGREGARPRODUCTOActionPerformed

    private void jButt_ACTUALIZARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt_ACTUALIZARActionPerformed
        try{
            if(this.jText_NM.getText().isEmpty() || this.jText_DM.getText().isEmpty() || this.jText_PM.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Falta información en algunos campos!");
            }else{
                boolean modificar = false;
              int Clave = Integer.parseInt(this.jSpinn_BUSCAR.getValue().toString());
              for(int i = 0; i < modeloTabla.getRowCount(); i++){
                int Cx = Integer.parseInt(this.jTab_PRODUCTOS.getValueAt(i, 0).toString());
                if(Clave == Cx){
                     modificar = true;
                     modeloTabla.removeRow(i);
                    }
                }
                if(modificar == true){
                    modeloTabla.insertRow(modeloTabla.getRowCount(), new Object[]{
                    this.jSpinn_BUSCAR.getValue(), this.jText_NM.getText(),
                    this.jText_DM.getText(),this.jText_PM.getText()});
                }else{
                    JOptionPane.showMessageDialog(null, "La clave seleccionada no coincide con ningúna.");
                }
            }
        }catch(RuntimeException ex){
            
        }
    }//GEN-LAST:event_jButt_ACTUALIZARActionPerformed

    private void jButt_BORRARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt_BORRARActionPerformed
        try{
            if(this.jTab_PRODUCTOS.getRowCount() == 0){
                JOptionPane.showMessageDialog(null, "No hay ningún producto en la lista");
            }
            int Clave = Integer.parseInt(this.jSpinn_BUSCAR.getValue().toString());
            for(int i = 0 ; i < modeloTabla.getRowCount(); i++){
                int Cx = Integer.parseInt(this.jTab_PRODUCTOS.getValueAt(i, 0).toString());
                if(Clave == Cx){
                    int r = JOptionPane.showConfirmDialog(this, "¿Seguro que desea ELIMINAR el producto?","Borrar",JOptionPane.YES_OPTION, JOptionPane.WARNING_MESSAGE);
                    if(r == JOptionPane.YES_OPTION){
                        modeloTabla.removeRow(i);
                    }
                }
            }
        }catch(RuntimeException ex){
            
        }
    }//GEN-LAST:event_jButt_BORRARActionPerformed

