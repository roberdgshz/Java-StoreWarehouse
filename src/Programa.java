    private void jButt_BUSCARPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButt_BUSCARPActionPerformed
        try{
            int []vec = new int[this.jTab_PRODUCTOS.getRowCount()];
            
            for(int i = 0; i < vec.length; i++){
                vec[i] = Integer.parseInt(modeloTabla.getValueAt(i, 0).toString());
            }
            
            Número p = new Número();
            p.n = 0;
            
            Boolean b = new Boolean();
            b.b = false;
            
            Métodos.Binario(vec, Integer.parseInt(this.jSpinn_IDB.getValue().toString()), p, b);
            
            if(b.b == true){
                this.jLab_POSICIÓN.setText(""+(p.n+1));
            }else if(b.b == false){
                JOptionPane.showMessageDialog(null, "No existe un producto con este ID: "+this.jSpinn_IDB.getValue());
            }
        }catch(RuntimeException e){
            
        }
    }//GEN-LAST:event_jButt_BUSCARPActionPerformed

